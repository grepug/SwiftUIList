import SwiftUI

struct EmojiPickerPopover: ViewModifier {
    @Binding var isPresented: Bool
    @Binding var selection: String
    
    func body(content: Content) -> some View {
        content
            .popover(isPresented: $isPresented, arrowEdge: .trailing) {
                EmojiPicker(selection: $selection)
            }
            .onTapGesture {
                isPresented = true
            }
            .onChange(of: selection) { newValue in
                isPresented = false
            }
    }
}

struct EmojiPicker: View {
    @Binding var selection: String
    
    @State var groupIndex = 0
    
    var group: [String] {
        Self.groups[groupIndex]
    }
    
    var body: some View {
        VStack {
            Text(selection)
                .font(.system(size: 44))
            
            Picker("", selection: $groupIndex) {
                ForEach(0..<Self.groups.endIndex) { index in
                    Text(Self.groups[index][0])
                        .tag(index)
                }
            }
            .pickerStyle(.segmented)
            
            ScrollView {
                LazyVGrid(columns: [
                    .init(.adaptive(minimum: 30))
                ]) {
                    ForEach(group, id: \.self) { emoji in
                        button(emoji)
                    }
                }
            }
        }
        .padding()
        .frame(width: 450, height: 500)
    }
    
    func button(_ string: String) -> some View {
        Button {
            selection = string
        } label: {
            Text(string)
                .font(.title2)
        }
        .buttonStyle(.borderless)
    }
}

